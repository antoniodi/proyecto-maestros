    public DetaMaestro(Maestro maestro, Destreza destreza, Cliente cliente) {
        initComponents();
        this.destreza = destreza;
        this.maestro = maestro;
        this.cliente = cliente;
        String[] titulos = {"Autor", "Comentario", "Calificaci√≥n", "Municipio"};
        model = new DefaultTableModel(null, titulos);
        
        jLabel1.setText(maestro.getNombre()+" - "+destreza.getHabilidad().toString()+" - "+destreza.getPromedio()+"");
        /*
        agregamos a la tabla los comentarios que le han realizado al maestro
        */
        for (Calificacion calificacion : destreza.getCalificaciones()) {
            String[] fila = {calificacion.getComentario().getAutor(),calificacion.getComentario().getDescripcion(),calificacion.getValoracion()+"",maestro.getMunicipio().toString()};
            model.addRow(fila);
        }
        jTable1.setModel(model);
        
        
    }

